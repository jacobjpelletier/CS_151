//A program to demonstrate different usses of the "+" operator:
//   1. For performing string concatenation.
//   2. For adding two numbers.

class AddOperator {
  
   public static void main (String[] args) {

     System.out.print ("These are " + "the decimal ");
     System.out.println ("digits: " + 1234567890);
     System.out.println();
     System.out.print ("This is " + "the result of ");
     System.out.println("adding two numbers: " + (12345 + 6789));

   }
}

